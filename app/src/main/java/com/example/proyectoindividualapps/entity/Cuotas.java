package com.example.proyectoindividualapps.entity;

public class Cuotas {
    private Integer primera;
    private Integer segunda;
    private Integer tercera;
    private Integer cuarta;
    private Integer quinta;


    public Integer getPrimera() {
        return primera;
    }

    public void setPrimera(Integer primera) {
        this.primera = primera;
    }

    public Integer getSegunda() {
        return segunda;
    }

    public void setSegunda(Integer segunda) {
        this.segunda = segunda;
    }

    public Integer getTercera() {
        return tercera;
    }

    public void setTercera(Integer tercera) {
        this.tercera = tercera;
    }

    public Integer getCuarta() {
        return cuarta;
    }

    public void setCuarta(Integer cuarta) {
        this.cuarta = cuarta;
    }

    public Integer getQuinta() {
        return quinta;
    }

    public void setQuinta(Integer quinta) {
        this.quinta = quinta;
    }
}
